public class Massiv2 {
    public static void main(String[] args) {
        //Через ключевое слово new в переменную nums присваиваем пустой массив на 10 элементов
        int [] nums = new int[10];

        //Когда происходит заход в цикл, на первой итерации i равно 0
        for (int i = 0; i < nums.length; i++) {
            //Получается, что выводить мы будем элемент nums[0]
            //На каждой следующей итерации i увеличивается на 1, таким образом мы проходимся по всему массиву
            System.out.println(nums[i]);
            // jngdfbdlkf
            //kvjdfnvkdfjv
        }
    }
}
